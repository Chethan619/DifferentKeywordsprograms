public class Widening_Primtc_July09 //widening concept
{
   public static void main(String[] args) 
   {
	   double d1=100; // converting int datatype to double data type. This is implicit execution
	   System.out.println(d1);
	   double d2=(double)200; // converting int datatype to double data type. This is explicit execution
	   System.out.println(d2);
   }
}
